package com.everis.restful.model.entity;

public class AuthorEntity {
	
	private String name;
	private String email;
	
	public AuthorEntity() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
